    private void generateReport() {
        DateFormat Df = new SimpleDateFormat("yyyy-MM-dd");
        String gStatus =(String)getStatus.getSelectedItem();
        generateReportController Grp = new generateReportController();
        Grp.fromDate = Df.format(fromDate.getDate());
        Grp.toDate = Df.format(toDate.getDate());
        Grp.Status = gStatus;
        FDateString = Df.format(fromDate.getDate());
        TDateString = Df.format(toDate.getDate());
        AvgTxt.setText(String.valueOf(Grp.AveData()));
        Orders Or = Grp.HighP();
        TxtPhone.setText(Or.getPhone());
        TxtHprice.setText(String.valueOf(Or.getHightPrice()));
        MaxItem.setText(Or.getMaxItemName());
        Maxquantity.setText(String.valueOf(Or.getMaxQuantity()));
        MinItem.setText(Or.getMinItemName());
        MinQuantity.setText(String.valueOf(Or.getLowerQuantity()));
        
        DefaultTableModel dtm = (DefaultTableModel) ReportTable.getModel();
        dtm.setRowCount(0);

        Iterator<Orders> itrIt = OwnerViewController.ViewTable(FDateString, TDateString,gStatus);
        while (itrIt.hasNext()) {
            Orders oObj = itrIt.next();
            dtm.addRow(new Object[]{oObj.getId(), oObj.getPhone(), oObj.getDate(), oObj.getTotal(), oObj.getDiscount(), oObj.getStatus()});
        }
        show1.setVisible(true);
        show2.setVisible(true);
        show3.setVisible(true);
        show4.setVisible(true);
    }

